// Examples of valid JavaScript identifiers:
var name = "Alice";         // starts with a letter
var _count = 5;              // starts with underscore
var $price = 9.99;           // starts with dollar sign
var item2 = "book";         // can contain letters and digits after the first character
var total_value = 100;       // can contain underscores after the first character

console.log(name, _count, $price, item2, total_value);

